# Listas
frutas = ["manzana", "banana", "cereza"]
print(frutas[0])  # Acceso al primer elemento
frutas.append("durazno")  # Añadir un elemento
print(frutas)

# Diccionarios
persona = {"nombre": "Ana", "edad": 25}
print(persona["nombre"])  # Acceso al valor del nombre
persona["altura"] = 1.75  # Añadir un nuevo par clave-valor
print(persona)